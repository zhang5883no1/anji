public class OnlineQADto {
	private Long id;
	private Long qi;
	private Long ai;
	private String qt;
	private String at;
	private String qn;
	private String an;
	private int status;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getQi() {
		return qi;
	}
	public void setQi(Long qi) {
		this.qi = qi;
	}
	public Long getAi() {
		return ai;
	}
	public void setAi(Long ai) {
		this.ai = ai;
	}
	public String getQt() {
		return qt;
	}
	public void setQt(String qt) {
		this.qt = qt;
	}
	public String getAt() {
		return at;
	}
	public void setAt(String at) {
		this.at = at;
	}
	public String getQn() {
		return qn;
	}
	public void setQn(String qn) {
		this.qn = qn;
	}
	public String getAn() {
		return an;
	}
	public void setAn(String an) {
		this.an = an;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}	
	
}
